export function t(key, language = 'zh-CN') {
  const keys = key.split('.')
  let value = translations[language]
  
  for (const k of keys) {
    if (value && value[k]) {
      value = value[k]
    } else {
      return key
    }
  }
  
  return value || key
}

export function createI18n(computedLanguage) {
  return {
    t: (key) => {
      return t(key, computedLanguage.value)
    }
  }
}